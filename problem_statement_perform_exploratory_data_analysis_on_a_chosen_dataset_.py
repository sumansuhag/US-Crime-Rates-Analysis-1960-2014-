import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

"""# Assuming the dataset is in CSV format after cleaning."""

# Load the dataset
data = pd.read_csv('US_Crime_Rates_1960_2014.csv')

"""Generate Descriptive Statistics and Visualizations"""

# Generate descriptive statistics
stats = data.describe()
print(stats)

"""# Create visualizations to understand the data better."""

# Set the style for seaborn
sns.set(style="whitegrid")

# Plotting Total Crime Rates Over the Years
plt.figure(figsize=(12, 6))
sns.lineplot(data=data, x='Year', y='Total', marker='o')
plt.title('Total Crime Rates in the US (1960-2014)')
plt.xlabel('Year')
plt.ylabel('Total Crimes')
plt.xticks(rotation=45)
plt.show()

"""# Identify Patterns, Trends, and Anomalies in the Data"""

# Plotting Violent Crime Rates Over the Years
plt.figure(figsize=(12, 6))
sns.lineplot(data=data, x='Year', y='Violent', marker='o', label='Violent Crimes')
sns.lineplot(data=data, x='Year', y='Property', marker='o', label='Property Crimes')
plt.title('Violent vs Property Crime Rates in the US (1960-2014)')
plt.xlabel('Year')
plt.ylabel('Number of Crimes')
plt.legend()
plt.xticks(rotation=45)
plt.show()

"""# Look for seasonal patterns or cyclical trends in the data."""

# Correlation heatmap to identify relationships
plt.figure(figsize=(10, 8))
sns.heatmap(data.corr(), annot=True, fmt=".2f", cmap='coolwarm')
plt.title('Correlation Heatmap of Crime Rates')
plt.show()

"""Identify any outliers or unusual spikes in crime rates."""

# Boxplot to identify outliers in Violent Crimes
plt.figure(figsize=(12, 6))
sns.boxplot(data=data['Violent'])
plt.title('Boxplot of Violent Crimes to Identify Outliers')
plt.show()

